"""Generate non-destructive audit reports from the dataset statistics CSV.

This script only reads the report produced by dataset_stats.py and writes
derived audit files under results/ without altering the dataset.
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path
from typing import TypedDict, cast

import pandas as pd
from tqdm import tqdm


def detect_project_root(start_path: Path) -> Path:
    """Walk upward until the repository root is located."""

    for candidate in [start_path, *start_path.parents]:
        if (candidate / "configs" / "config.py").is_file():
            return candidate
    raise RuntimeError("Unable to locate PROJECT_ROOT for dataset_cleaner.py")


PROJECT_ROOT = detect_project_root(Path(__file__).resolve())
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from configs.config import RESULTS_DIR  # noqa: E402


class AuditData(TypedDict):
    """Container for the derived audit artifacts."""

    empty_folders: list[str]
    corrupted_rows: list[dict[str, int | str]]
    low_image_df: pd.DataFrame
    high_image_df: pd.DataFrame


def build_parser() -> argparse.ArgumentParser:
    """Create the command line interface for the audit script."""

    parser = argparse.ArgumentParser(description="Generate dataset audit reports.")
    parser.add_argument(
        "--report-csv",
        type=Path,
        default=RESULTS_DIR / "dataset_report.csv",
        help="Path to the dataset report generated by dataset_stats.py.",
    )
    parser.add_argument(
        "--results-dir",
        type=Path,
        default=RESULTS_DIR,
        help="Directory where derived audit reports will be saved.",
    )
    return parser


def load_report(report_csv: Path) -> pd.DataFrame:
    """Load the statistics report and validate the expected columns."""

    if not report_csv.exists():
        raise FileNotFoundError(f"Dataset report not found: {report_csv}")

    report_df = pd.read_csv(report_csv)
    expected_columns = {"class_name", "image_count", "corrupted_images", "total_files", "empty_folder"}
    missing_columns = expected_columns.difference(report_df.columns)
    if missing_columns:
        missing = ", ".join(sorted(missing_columns))
        raise ValueError(f"dataset_report.csv is missing required columns: {missing}")

    return report_df


def audit_report(report_df: pd.DataFrame) -> dict[str, object]:
    """Collect the requested slices of the dataset report."""

    empty_folders: list[str] = []
    corrupted_rows: list[dict[str, int | str]] = []
    low_image_rows: list[dict[str, object]] = []
    high_image_rows: list[dict[str, object]] = []

    for row in tqdm(
        report_df.itertuples(index=False, name=None),
        total=len(report_df),
        desc="Auditing classes",
        unit="class",
    ):
        class_name = str(row[0])
        image_count = int(row[1])
        corrupted_images = int(row[2])
        empty_folder = bool(row[4])

        # Collect classes that need attention without changing any files.
        if empty_folder or image_count == 0:
            empty_folders.append(class_name)
        if corrupted_images > 0:
            corrupted_rows.append({"class_name": class_name, "corrupted_images": corrupted_images})
        if image_count < 5:
            low_image_rows.append({"class_name": class_name, "image_count": image_count})
        if image_count > 20:
            high_image_rows.append({"class_name": class_name, "image_count": image_count})

    audit_data: AuditData = {
        "empty_folders": empty_folders,
        "corrupted_rows": corrupted_rows,
        "low_image_df": pd.DataFrame(low_image_rows, columns=["class_name", "image_count"]),
        "high_image_df": pd.DataFrame(high_image_rows, columns=["class_name", "image_count"]),
    }
    return cast(dict[str, object], audit_data)


def save_text_report(lines: list[str], output_path: Path) -> None:
    """Persist a plain-text report using UTF-8 encoding."""

    output_path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def format_empty_folders(empty_folders: list[str]) -> list[str]:
    """Format the empty folder list for a readable text report."""

    if not empty_folders:
        return ["None"]
    return empty_folders


def main() -> None:
    """Run the dataset audit and generate the four requested reports."""

    args = build_parser().parse_args()
    report_df = load_report(args.report_csv)
    audit = audit_report(report_df)
    empty_folders = cast(list[str], audit["empty_folders"])
    corrupted_rows = cast(list[dict[str, int | str]], audit["corrupted_rows"])
    low_image_df = cast(pd.DataFrame, audit["low_image_df"])
    high_image_df = cast(pd.DataFrame, audit["high_image_df"])

    results_dir = args.results_dir
    results_dir.mkdir(parents=True, exist_ok=True)

    empty_folders_path = results_dir / "empty_folders.txt"
    corrupted_images_path = results_dir / "corrupted_images.txt"
    low_image_classes_path = results_dir / "low_image_classes.csv"
    high_image_classes_path = results_dir / "high_image_classes.csv"

    # Text reports are written as audit-friendly line lists.
    save_text_report(empty_folders, empty_folders_path)

    corrupted_lines = ["class_name,corrupted_images"]
    corrupted_lines.extend(
        f"{row['class_name']},{row['corrupted_images']}" for row in corrupted_rows
    )
    save_text_report(corrupted_lines, corrupted_images_path)

    low_image_df.to_csv(low_image_classes_path, index=False)
    high_image_df.to_csv(high_image_classes_path, index=False)

    print("Empty folders:")
    for class_name in format_empty_folders(empty_folders):
        print(class_name)

    print("Corrupted images:")
    if corrupted_rows:
        for row in corrupted_rows:
            print(f"{row['class_name']}: {row['corrupted_images']}")
    else:
        print("None")

    print("Classes having fewer than 5 images:")
    if not low_image_df.empty:
        print(low_image_df.to_string(index=False))
    else:
        print("None")

    print("Classes having more than 20 images:")
    if not high_image_df.empty:
        print(high_image_df.to_string(index=False))
    else:
        print("None")

    print(f"Saved: {empty_folders_path}")
    print(f"Saved: {corrupted_images_path}")
    print(f"Saved: {low_image_classes_path}")
    print(f"Saved: {high_image_classes_path}")
    print("Dataset Audit Complete.")


if __name__ == "__main__":
    main()